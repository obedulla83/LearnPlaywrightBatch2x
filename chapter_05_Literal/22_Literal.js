let age = "pramod"; // this is a string literal assigned to a variable age
let isStudent = true; //this is a boolean literal assigned to a variable isStudent
let pi = 3.14; // this is a number literal assigned to a variable pi
let name = 'Alice'; // this is a string literal assigned to a variable name
let nullValue = null; // this is a null literal assigned to a variable nullValue
let undefinedValue; // this is an undefined literal assigned to a variable undefinedValue

// typeof operator is used to determine the type of a variable or value in JavaScript
console.log(typeof age);
console.log(typeof isStudent);
console.log(typeof pi);
console.log(typeof name);
console.log(typeof nullValue);
console.log(typeof undefinedValue);